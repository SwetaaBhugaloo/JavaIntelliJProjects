public class SwitchCases {
    public static void main(String[] args) {
        char choice = 'a';
        switch (choice){
            case 'a':
                System.out.println("a");
                System.out.println("a");
                break;
            case 'b':
                System.out.println("b");
                System.out.println("b");
                break;
            case 'c':
                System.out.println("c");
                System.out.println("c");
                break;
            default:
                System.out.println("d");
                break;
        }
    }
}
